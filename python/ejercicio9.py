#Ejercicio8: Bucles while y sumas:

# 1. Variable acumuladora:

total = 0

#Ahora que sabemos que la suma empieza en 0:

#Variable de control:

numero = 1

#Condición del bucle:

while numero <= 10:

#Acumulación dentro del bucle:

	total += numero

#Actualizar la variabnle de control

	numero += 1

#Mostrar resultado:

print(f"El resultado de la suma del 1 al 10 es {total}")

